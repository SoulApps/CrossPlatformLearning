ORG 1000H		;ESPACIO DE DIRECCIONES
a1 DB 20		;DECLARAR UNA VARIABLE DE 8 BITS (DB) CON EL PRIMER VALOR
a2 DB 5		
dire DW 3000H	;DECLARO UNA VARIABLE DE 16(DW) BITS CON LA DIRECCION DEL RESULTADO

ORG 1500H		;ESPACIO DE DIRECCIONES DE LAS INSTRUCCIONES
MOV AL, a1		;CARGAR 20d (0X14) EN AL
MOV BL, a2		;CARGAR 5d (0X5) EN BL
MOV CL, AL		;COPIAR AL EN CL
ADD AL, BL		;SUMAR AL=AL+BL
MOV BX, dire	;CARGAR LA DIRECCION DE "dire"(1000) EN BX
MOV [BX], AL	;GUARDAR EN LA DIRECCION 3000H EL VALOR AL

;EN ESTE PUNTO 	AL=25d(19h)
;				BL=00d(00h)
;				BX=1000H
;				CL=20d
;				1000H-->3000H
;				3000H ([BX])-->25d (0X19)

END
